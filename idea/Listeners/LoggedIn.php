<?php

namespace Idea\Listeners;

use Illuminate\Auth\Events\Login;

class LoggedIn
{
    /**
     * Handle the event.
     *
     * @param  \Illuminate\Auth\Events\Login  $event
     * @return void
     */
    public function handle(Login $event)
    {
        $user = $event->user;
        $user->last_login_at = date('Y-m-d H:i:s');
        $user->save();
    }
}